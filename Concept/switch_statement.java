public class switch_statement {
    public static void main(String[] args){
        String a = "A";

        switch (a){
            case "a" :
                System.out.println("a"); break;
            case ("B") :
                System.out.println("B");
                break;
            case "A" :
                System.out.println("A"); break;
            default :
                System.out.println("Input now correct");
        }


    }
}
